// Operadores em JavaScript 2

// Relacionais             (Sempre recebe valor Boolean)
//                        (Pode usar com variavel)
// 5>4   =  true
// 7<4   =  false
// 8>=8  =  true
// 9<=7  =  false
// 5==5  =  true
// 4!=4  =  false

// Indentificadores:

//  5 == 5     true    | 5 != 5     false  |  5 !=  4    true
//  5 == "5"   true    | 5 != "5"   false  |  5 !=  "4"  true
//  5 === "5"  false   | 5 !== "5"  true   |  5 !== "4"  true
//  5 === 5    treu    | 5 !== 5    false  |  5 !== 4    true

// Logicos

//   ! negação       !true  = false
//                   !false = true

//  && conjunção     true  && true   = true
//                   true  && false  = false
//                   false && true   = false
//                   false && true   = false

//  || disjunção     true  || true   = true
//                   true  || false  = true
//                   false || false  = false
//                   true  || false  = true

